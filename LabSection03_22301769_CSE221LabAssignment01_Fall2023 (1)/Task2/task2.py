data = list(map(int, infile.readline().strip().split()))

def bubbleSort(arr):
    for i in range(len(arr)-1):
        swp = False
        for j in range(len(arr)-i-1):
            if arr[j] > arr[j+1]:
                arr[j], arr[j+1] = arr[j+1], arr[j]
                swp = True
        if(swp == False):
            break

    return arr

for i in (bubbleSort(data)):
    outfile.write(f'{i} ')
